#include<stdio.h>
int main()
{
int num,i,choice; //print multiplication table of a given number.
printf("Enter a number:");
scanf("%d",&num);
printf("Enter your choice(1 to print table,0 to exit)");
scanf("%d",&choice);
switch(choice){
  case 1:
     printf("Multiplication table of %d:\n",num);
     for(i=1;i<=10;i++){
         printf("%d X %d = %d\n",num,i,num*i);}
     break;
 case 0:
    printf("Exit from program.\n");
    break;
 default:
    printf("Invalid choice! please enter 1 or 0.\n");
 }
 return 0;
}
