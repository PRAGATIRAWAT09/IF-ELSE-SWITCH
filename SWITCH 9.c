#include <stdio.h>
void main() 
{
int num;
printf("Enter any number :"); // check even or odd using switch.
scanf("%d", &num);
switch (num % 2) {
        case 0:
            printf("The number %d is Even.\n", num);
            break; 
        case 1:
        case -1:
            printf("The number %d is Odd.\n", num);
            break;
    }
getch();
}
