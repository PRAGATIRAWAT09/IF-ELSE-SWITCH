#include <stdio.h>
int main() 
{
int marks;
printf("Enter your marks (0-100): "); // display grades based on marks.
scanf("%d", &marks);
int gradeCategory = marks / 10;
switch (gradeCategory) {
        case 10: // Marks 100
        case 9:  // Marks 90-99
            printf("Your grade is A\n");
            break;
        case 8:  // Marks 80-89
            printf("Your grade is B\n");
            break;
        case 7:  // Marks 70-79
            printf("Your grade is C\n");
            break;
        case 6:  // Marks 60-69
            printf("Your grade is D\n");
            break;
        default: // Marks 0-59
            printf("Your grade is F\n");
            break;
    }
 return 0;
}
