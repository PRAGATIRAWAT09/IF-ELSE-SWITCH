#include <stdio.h>
void main()  
{
char ch;
printf("Enter an alphabet: "); // vowel or consonant using switch.
scanf(" %c", &ch);
if (ch >= 'a' && ch <= 'z') {
      switch (ch) {
            case 'a':
            case 'e':
            case 'i':
            case 'o':
            case 'u':
                printf("%c is a vowel.\n", ch);
                break;
            default:
                printf("%c is a consonant.\n", ch);
                break;
        }
    } 
else 
    printf("%c is not an alphabet.\n", ch);
getch();
}
